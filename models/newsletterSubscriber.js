import { Schema, model } from 'mongoose';

const newsletterSubscriberSchema = new Schema({
    firstname: {
        type: String,
        required: true
    },
    lastname: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    phone: {
        type: String,
        required: true
    }
});

export default model('newsletterSubscriber', newsletterSubscriberSchema);
