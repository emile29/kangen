import { Schema, model } from 'mongoose';

const ebookUser = new Schema({
    firstname: {
        type: String,
        required: true
    },
    lastname: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    phone: {
        type: Number,
        required: true
    }
});

export default model('ebookUser', ebookUser);
